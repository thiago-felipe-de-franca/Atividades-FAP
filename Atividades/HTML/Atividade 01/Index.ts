//     <body>: tag body e onde fica o corpo do codigo

// Exercicio 2
// R->As tags <ul> e <ol> servem para criar listas onde o objetivo e colocar de maneira linear um conjunto de valores, <ol> ou ordered list e uma boa opcao para numerar uma sequencia de passos como por exemplo uma receita, ja a tag <ul> ou unordered list serve para quando a ordem dos valores nao e tao relevante como por exemplo numa lista de compras
