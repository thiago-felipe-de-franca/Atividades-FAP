// Vinculando páginas, arquivos e âncoras

// Exercicio 1
// R-> A tag <a> cria links em HTML, usando o atributo href para especificar o destino. É útil para vincular páginas, arquivos ou âncoras dentro da mesma página. O atributo target controla como o link é aberto, por exemplo, em uma nova aba usando _blank.
// 
// Exercicio 2
// R-> Além de href, a tag <a> pode ter atributos como title e rel. O atributo target determina como o link é exibido, permitindo, por exemplo, abrir em uma nova janela com _blank.
// 

// Incorporando imagens e mídias

// Exercicio 1
// R-> Os atributos src e alt na tag <img> definem a fonte da imagem e um texto alternativo, respectivamente. O alt é crucial para acessibilidade e SEO.
// 
// Exercicio 2
// R-> Além de imagens, mídias como vídeos e áudios podem ser incorporados com <video> e <audio>. Para garantir responsividade, deve-se usar atributos como width e height em conjunto com CSS responsivo.
// 

// Criando tabelas básicas

// Exercicio 1
// R-> As principais tags para tabelas são <table>, <tr>, <th> e <td>. <th> define cabeçalhos e <td> define células de dados; <th> é usada para destacar títulos ou categorias.
// 
// Exercicio 2
// R-> Tabelas podem ser complexas e difíceis de adaptar a diferentes dispositivos, por isso, outras formas de apresentação como listas ou grids com CSS são preferidas para layouts. No entanto, as tabelas são apropriadas para organizar dados numéricos ou informações que necessitam de uma estrutura tabular clara, como planilhas e comparações de preços.
// 
